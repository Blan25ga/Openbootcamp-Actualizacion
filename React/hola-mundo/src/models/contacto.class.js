export class Contacto {
    nombre = "";
    apellido = "";
    email = "";
    conectado = false;

    constructor(nombre, apellido, email, conectado) { // constructor con iniciacion de cada una de las propiedades
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.conectado = conectado;
    }
}





